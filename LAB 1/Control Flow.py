# If/else statement example 
x = 10
if x > 0:  # Check if x is positive
  print("x is positive")
else:
  print("x is non-positive")

# If/elif/else statement example

y = -5
if y > 0:  # Check if y is positive
  print("y is positive")
elif y == 0:  # Check if y is zero
  print("y is zero")
else:
  print("y is negative")


